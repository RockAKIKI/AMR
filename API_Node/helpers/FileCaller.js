/*
    Lire et écrire des données json.
*/

// //////////////////////////////////////////////////////
// LIBRAIRIES
// //////////////////////////////////////////////////////

// filesystem, librairie native
const {readFileSync, writeFileSync} = require('fs'); 

// //////////////////////////////////////////////////////
// FONCTIONS
// //////////////////////////////////////////////////////

function FileLoad(path)
{
    return JSON.parse(readFileSync(path, 'utf-8'))
};

function FileSave(path, liste)
{
    const objJSON = JSON.stringify(liste);
    writeFileSync(path,objJSON);
}

// //////////////////////////////////////////////////////
// EXPORTS
// //////////////////////////////////////////////////////

module.exports = {
    FileLoad,
    FileSave
}